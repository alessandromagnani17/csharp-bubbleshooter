namespace csharp_tasks.Accursi_Giacomo
{    /// <summary>
     /// Class that represents a 2D point.
     /// </summary>
    public class Point2D
    {
        public double X { get;}
        public double Y { get;}

        public Point2D(double x, double y)
        {
            X = x;
            Y = y;
        }
    }
}